'''
Questão 05 - Escreva um programa que calcule qual o salário de um
trabalhador que ganha R$ 1500,00 por mês e que irá receber
um aumento de 5%. Qual o salário após o aumento?
'''
# Valor do salario antes do aumento
salario_old = 1500.00;
# Calcula o salario novo com aumento de 5%
salario_new = salario_old * 1.05;
# Mostra o valor do novo salario
print(f'O novo salario é: {salario_new:.2f}');
